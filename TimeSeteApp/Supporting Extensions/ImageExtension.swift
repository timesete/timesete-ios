import UIKit

extension UIImage {
    
    static var logoLogin: UIImage {
        return UIImage(named: "logo-login") ?? UIImage()
    }
    
    static var pageOne: UIImage {
        return UIImage(named: "page-one") ?? UIImage()
    }
    
    static var pageTwo: UIImage {
        return UIImage(named: "page-two") ?? UIImage()
    }

}
